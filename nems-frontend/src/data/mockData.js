// Derived helpers
export const getStudentById = (id) => students.find((s) => s.student_id === parseInt(id));
export const getExamById = (id) => exams.find((e) => e.exam_id === parseInt(id));
export const getCentreById = (id) => examCentres.find((c) => c.centre_id === parseInt(id));
export const getScheduleById = (id) => schedules.find((s) => s.schedule_id === parseInt(id));
export const getRegistrationById = (id) => registrations.find((r) => r.registration_id === parseInt(id));

export const getStudentFullName = (id) => {
  const s = getStudentById(id);
  return s ? `${s.first_name} ${s.last_name}` : "Unknown";
};

export const getExamName = (id) => {
  const e = getExamById(id);
  return e ? e.exam_name : "Unknown";
};

